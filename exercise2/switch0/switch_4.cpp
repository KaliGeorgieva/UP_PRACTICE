#include <iostream>

using namespace std;

int main()
{
    int a;

    cout << "Enter number:" << endl;
    cin >> a;

    (a%2!=0) ? cout << "a%2!=0" << endl : cout << "a%2=0" << endl;
    (a%3!=0) ? cout << "a%3!=0" << endl : cout << "a%3=0" << endl;
    (a%4!=0) ? cout << "a%4!=0" << endl : cout << "a%4=0" << endl;
    (a%5!=0) ? cout << "a%5!=0" << endl : cout << "a%5=0" << endl;
    (a%6!=0) ? cout << "a%6!=0" << endl : cout << "a%6=0" << endl;
    (a%7!=0) ? cout << "a%7!=0" << endl : cout << "a%7=0" << endl;
    (a%8!=0) ? cout << "a%8!=0" << endl : cout << "a%8=0" << endl;
    (a%9!=0) ? cout << "a%9!=0" << endl : cout << "a%9=0" << endl;
    (a%10!=0) ? cout << "a%10!=0" << endl : cout << "a%10=0" << endl;

    return 0;
}
